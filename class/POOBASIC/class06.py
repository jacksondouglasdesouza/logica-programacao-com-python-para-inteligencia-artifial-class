#O programa deve fazer a leitura da largura e altura de um triângulo;
#O sistema deve informar no prompt a área do triângulo.

larguraTriangulo = float(input("Digite a largura do triângulo: "))
alturaTriangulo  = float(input("Digite a altura do Triângulo:  "))
areaTriangulo = (larguraTriangulo * alturaTriangulo) / 2

print("A área do triângulo é de: ", areaTriangulo)



